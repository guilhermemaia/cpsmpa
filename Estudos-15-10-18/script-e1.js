// Função de soma

function soma (a, b){
    return a + b;
}
    
document.getElementById("resultado-soma").innerHTML = soma(4, 25) + "  -  Resultado da Soma";

// Função multiplicação

function multiplicacao (a, b){
    return a * b
}

document.getElementById("resultado-multiplicação").innerHTML = multiplicacao (5, 10) + "  -  Resultado da Multiplicação";

// Função divisão

function divisão (a, b){
    return a / b
}

document.getElementById("resultado-divisao").innerHTML = divisão (1200, 6) + "  -  Resultado da Divisão";